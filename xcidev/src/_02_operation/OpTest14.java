package _02_operation;

public class OpTest14 {
	public static void main(String[] args) {
		
		
		// 명시적형변환, 강제형변환, down casting
		int n = (int) 3.14;
		int m = (int) 3.998; // 버림
		
		System.out.println(n);
		System.out.println(m);
		
		
		// 자동형변환, 묵시적형변환, up casting
		double d = 10;
		System.out.println(d);
		
		
		
		
	}
}
