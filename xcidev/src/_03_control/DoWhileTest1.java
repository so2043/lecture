package _03_control;

public class DoWhileTest1 {
	public static void main(String[] args) {
		
		
		int i = 1;
		while (i <= 3) {
			System.out.println("sysone");
			i++;
		}
		
		
		
		do {
			System.out.println("sysone");
			i++;
		} while (i <= 3);
			
		
		
		
		
		System.out.println("종료");
	}
}
